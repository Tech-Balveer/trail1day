# Write a python program to generate a series in descending order starting at number 100 to 0, by interval 5.


print("\n\nSeries in descending order starting at number 100 to 0 , by interval of 5 : ")
for x in range (100,-1,-5):
	print(x,end=" ")


print("\n\nSeries in acending order starting at number 0 to 100 , by interval of 5 : ")
for x in range (1,5,100):
	print(x,end=" ")
